"""Command line interface module."""

import argparse
import os

from .scenario import load_scenario

import logging

logger = logging.getLogger(__name__)


class CLI:
    """Command line interface class."""

    def __init__(self):
        """Initialize command line interface object."""

        self.parser = argparse.ArgumentParser(
            description="Benchmarking toolkit for geospatial processing workflows."
        )
        self.parser.add_argument("filename", type=str, help="Scenario filename")
        self.parser.add_argument("-n", "--name", type=str, help="Scenario name")
        self.parser.add_argument("-r", "--repeat", type=int, help="Number of repeats")
        self.parser.add_argument(
            "-w",
            "--workdir",
            type=str,
            help="Working directory (default = current working directory)",
        )
        self.parser.add_argument(
            "-b",
            "--basedir",
            type=str,
            help="Base directory (default = current working directory)",
        )
        self.parser.add_argument(
            "-o",
            "--outdir",
            type=str,
            help="Output directory (default = autogenerated from the scenario name)",
        )
        self.parser.add_argument(
            "-c", "--clean", action="store_true", help="Clean the output directory"
        )
        self.parser.add_argument(
            "-d", "--debug", action="store_true", help="Enable debug mode"
        )
        # TODO: Add more scenario arguments for further customization.

    def run(self):
        """Run command line interface."""
        args = self.parser.parse_args()

        if args.debug:
            logging.basicConfig(
                level=logging.DEBUG,
                format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
            )

            for name in logging.root.manager.loggerDict:
                if name.startswith("geobench"):
                    logging.getLogger(name).setLevel(logging.DEBUG)

            logger.debug("Debugging enabled")
        else:
            logging.basicConfig(
                level=logging.INFO, format="%(levelname)s - %(message)s"
            )

        kwargs = {key: val for key, val in vars(args).items() if val is not None}

        scenario = load_scenario(os.path.abspath(args.filename), **kwargs)
        scenario.benchmark(clean=args.clean)


def main():
    """Run command line interface."""
    cli = CLI()
    cli.run()
